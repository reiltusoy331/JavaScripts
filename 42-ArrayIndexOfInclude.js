// Useful Methods in Arrays

let fruits = ['apple','banana','orange','watermelon']


//indexOf
let findApple = fruits.indexOf('orange')
console.log(findApple)


//Index
let FindInclude = fruits.includes('watermelon')
console.log(FindInclude)

//slice
let SliceFruits = fruits.slice(1,3)
console.log(SliceFruits)